def validate_jwt(token: str) -> tuple[str | None, str | None]:
    try:
        decoded = jwt.decode(
            token,
            app.secret_key,
            algorithms=['HS512'],
            issuer='BTEC_REBEL_SYSTEM',
            options={'verify_iss': True, 'require': ['exp', 'user', 'role']}
        )
        return decoded['user'], None
    except jwt.ExpiredSignatureError:
        logger.warning("JWT validation failed: Token expired")
        return None, "TOKEN_EXPIRED"
    except jwt.InvalidIssuerError:
        logger.warning("JWT validation failed: Invalid issuer")
        return None, "INVALID_ISSUER"
    except jwt.MissingRequiredClaimError as e:
        logger.warning(f"JWT validation failed: Missing claim {e}")
        return None, f"INVALID_TOKEN_MISSING_CLAIM_{e}"
    except jwt.InvalidTokenError as e:
        logger.warning(f"JWT validation failed: Invalid token - {e}")
        return None, "INVALID_TOKEN"
    except Exception as e:
        logger.error(f"Unexpected error during JWT validation: {e}", exc_info=True)
        return None, "JWT_VALIDATION_ERROR"

def token_required(allowed_roles: list = None):
    def decorator(f):
        @wraps(f)
        def decorated_function(*args, **kwargs):
            token = None
            auth_header = request.headers.get('Authorization')
            if auth_header:
                try:
                    token = auth_header.split(" ")[1]
                except IndexError:
                    return jsonify({'message': 'Bearer token malformed'}), 401

            if not token:
                return jsonify({'message': 'Authorization token is missing'}), 401

            user_email, error = validate_jwt(token)
            if error:
                return jsonify({'message': f'Token error: {error}'}), 401

            g.user_email = user_email

            if allowed_roles:
                try:
                    decoded_token = jwt.decode(token, app.secret_key, algorithms=['HS512'], options={"verify_signature": False})
                    user_role = decoded_token.get('role')
                    if user_role not in allowed_roles:
                        log_audit("Authorization Denied", user_email, f"Role '{user_role}' not in allowed roles {allowed_roles} for {request.path}")
                        return jsonify({'message': 'Permission denied: Insufficient role'}), 403
                except Exception as e:
                    logger.error(f"Error during role check for {user_email}: {e}", exc_info=True)
                    return jsonify({'message': 'Error processing user role'}), 500

            return f(*args, **kwargs)
        return decorated_function
    return decorator

@app.route('/api/v1/register', methods=['POST'])
@limiter.limit("5 per hour")
def register():
    data = request.get_json()
    if not data or not all(k in data for k in ('email', 'password', 'role')):
        return jsonify({'error': 'Missing required fields: email, password, role'}), 400

    email = sanitize_input(data['email'])
    password = data['password']
    role = sanitize_input(data['role'])

    if '@' not in email or '.' not in email.split('@')[-1]:
        return jsonify({'error': 'Invalid email format'}), 400

    if role not in ['admin', 'evaluator', 'student']:
        return jsonify({'error': "Invalid role. Must be 'admin', 'evaluator', or 'student'"}), 400

    if len(password) < 8:
        return jsonify({'error': 'Password must be at least 8 characters long'}), 400

    hashed_pass = generate_password_hash(password)

    conn = None
    try:
        conn = get_db_conn()
        c = conn.cursor()
        c.execute("INSERT INTO users (email, password_hash, role) VALUES (?, ?, ?)",
                  (email, hashed_pass, role))
        conn.commit()
        log_audit("User Registration", email, f"User registered with role {role}")
        logger.info(f"User {email} registered successfully")
        return jsonify({'message': f'User {email} registered successfully'}), 201
    except sqlite3.IntegrityError:
        logger.warning(f"Registration failed: Email {email} already exists.")
        return jsonify({'error': f'Email {email} already exists'}), 409
    except sqlite3.Error as e:
        logger.error(f"Database error during registration: {e}", exc_info=True)
        return jsonify({'error': 'Registration failed due to database error'}), 500
    finally:
        if conn:
            conn.close()


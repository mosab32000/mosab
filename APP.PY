class SecureVault:
    def __init__(self):
        key = os.environ.get("DATA_ENCRYPTION_KEY")
        if not key:
            logger.critical("CRITICAL: DATA_ENCRYPTION_KEY environment variable not set. Cannot proceed securely.")
            key = Fernet.generate_key()
            logger.warning("Using temporary, insecurely generated DATA_ENCRYPTION_KEY for development ONLY.")
        else:
            try:
                key = key.encode()
                Fernet(key)
            except (ValueError, TypeError) as e:
                logger.critical(f"Invalid DATA_ENCRYPTION_KEY format: {e}. Key must be a URL-safe base64 encoded string.")
                raise ValueError("Invalid DATA_ENCRYPTION_KEY format") from e
        self.cipher_suite = Fernet(key)

    def encrypt(self, text: str) -> str:
        try:
            if not isinstance(text, str):
                text = str(text)
            encrypted_text = self.cipher_suite.encrypt(text.encode('utf-8'))
            return encrypted_text.decode('utf-8')
        except Exception as e:
            logger.error(f"Encryption failed: {e}", exc_info=True)
            raise ValueError("Encryption failed") from e

    def decrypt(self, encrypted_text: str) -> str:
        try:
            decrypted_text = self.cipher_suite.decrypt(encrypted_text.encode('utf-8'))
            return decrypted_text.decode('utf-8')
        except InvalidToken:
            logger.error("Decryption failed: Invalid token (key mismatch or corrupted data)")
            raise ValueError("Decryption failed: Invalid token")
        except Exception as e:
            logger.error(f"Decryption failed: {e}", exc_info=True)
            raise ValueError("Decryption failed") from e


def check_bias(text: str) -> bool:
    bias_terms = ['gender', 'race', 'religion', 'sex', 'sexual orientation', 'ethnicity', 'disability', 'age']
    text_lower = text.lower()
    return any(term in text_lower for term in bias_terms)

def sanitize_input(text: str) -> str:
    if not isinstance(text, str): return ""
    blacklist = [';', '--', '/*', '*/', 'xp_', '<script>', '</script>', 'SELECT ', 'INSERT ', 'UPDATE ', 'DELETE ', 'DROP ']
    sanitized_text = text
    for item in blacklist:
        sanitized_text = sanitized_text.replace(item, "")
    MAX_INPUT_LENGTH = 5000
    return sanitized_text[:MAX_INPUT_LENGTH]


@limiter.limit("10 per minute")
def login():
    data = request.get_json()
    if not data or 'email' not in data or 'password' not in data:
        return jsonify({'error': 'Missing email or password'}), 400

    email = sanitize_input(data['email'])
    password = data['password']

    conn = None
    try:
        conn = get_db_conn()
        c = conn.cursor()
        c.execute("SELECT password_hash, role FROM users WHERE email = ? AND is_active = TRUE", (email,))
        user_record = c.fetchone()

        if user_record and check_password_hash(user_record['password_hash'], password):
            issued_at = datetime.datetime.utcnow()
            expires_at = issued_at + datetime.timedelta(hours=int(os.environ.get("JWT_EXPIRY_HOURS", 2)))
            payload = {
                'user': email,
                'role': user_record['role'],
                'iat': issued_at,
                'exp': expires_at,
                'iss': 'BTEC_REBEL_SYSTEM'
            }
            token = jwt.encode(payload, app.secret_key, algorithm='HS512')
            log_audit("User Login", email, "Successful login")
            logger.info(f"User {email} logged in successfully")
            return jsonify({'token': token, 'expires_at': expires_at.isoformat() + "Z"})
        else:
            log_audit("User Login Failed", email, "Invalid credentials or inactive user")
            logger.warning(f"Login failed for user {email}: Invalid credentials or inactive user")
            return jsonify({'error': 'Invalid email or password'}), 401

    except sqlite3.Error as e:
        logger.error(f"Database error during login: {e}", exc_info=True)
        return jsonify({'error': 'Login failed due to database error'}), 500
    finally:
        if conn:
            conn.close()

